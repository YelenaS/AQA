package lessons;

public class IfElseIfStatement {
    public static void main(String[] args) {
        boolean condition1 = false;
        boolean condition2 = false;
        boolean condition3 = false;

        if (condition1) {
            System.out.println("We are satisfied condition1");
        } else if (condition2) {
            System.out.println("We are satisfied condition2");
        } else if (condition3) {
            System.out.println("We are satisfied condition3");
        } else {
            System.out.println("We are not satisfied any of conditions");
        }
    }
}
